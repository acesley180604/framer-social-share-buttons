export function getPlatformById(id: string): PlatformConfig | undefined {
    return PLATFORMS.find((p) => p.id === id)
}

export function appendUTMParams(
    url: string,
    utm: { enabled: boolean; source: string; medium: string; campaign: string },
    platformId?: string
): string {
    if (!utm.enabled) return url
    // Strip existing utm_ params first to avoid duplicates
    let cleanUrl = url
    try {
        const u = new URL(url, window.location.origin)
        u.searchParams.delete("utm_source")
        u.searchParams.delete("utm_medium")
        u.searchParams.delete("utm_campaign")
        cleanUrl = u.toString()
    } catch {
        // If URL parsing fails, fall through with original
    }
    const separator = cleanUrl.includes("?") ? "&" : "?"
    const source = utm.source === "auto" && platformId ? platformId : utm.source
    const params = [
        source ? `utm_source=${encodeURIComponent(source)}` : "",
        utm.medium ? `utm_medium=${encodeURIComponent(utm.medium)}` : "",
        utm.campaign ? `utm_campaign=${encodeURIComponent(utm.campaign)}` : "",
    ]
        .filter(Boolean)
        .join("&")
    return params ? `${cleanUrl}${separator}${params}` : cleanUrl
}

export function buildShareUrl(template: string, url: string, text: string): string {
    return template
        .replace("{url}", encodeURIComponent(url))
        .replace("{text}", encodeURIComponent(text))
}

export function buildFollowUrl(template: string, handle: string): string {
    return template.replace("{handle}", handle)
}
